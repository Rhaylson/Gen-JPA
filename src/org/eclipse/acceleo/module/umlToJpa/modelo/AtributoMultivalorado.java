public class AtributoMultivalorado{

	private Importe tipoLista;
	private Importe implementacao;
	
	public Importe getTipoLista() {
		return tipoLista;
	}
	
	public void setTipoLista(Importe tipoLista) {
		this.tipoLista = tipoLista;
	}
	
	public Importe getImplementacao() {
		return implementacao;
	}
	public void setImplementacao(Importe implementacao) {
		this.implementacao = implementacao;
	}
	
	
}
